package kr.or.ddit.dam.vo;


public class MileageVO {
	private int mileage_id;
	private String mem_mail;
	private String mileage_type;
	private int mileage_amt;
	private String mileage_date;
	public int getMileage_id() {
		return mileage_id;
	}
	public void setMileage_id(int mileage_id) {
		this.mileage_id = mileage_id;
	}
	public String getMem_mail() {
		return mem_mail;
	}
	public void setMem_mail(String mem_mail) {
		this.mem_mail = mem_mail;
	}
	public String getMileage_type() {
		return mileage_type;
	}
	public void setMileage_type(String mileage_type) {
		this.mileage_type = mileage_type;
	}
	public int getMileage_amt() {
		return mileage_amt;
	}
	public void setMileage_amt(int mileage_amt) {
		this.mileage_amt = mileage_amt;
	}
	public String getMileage_date() {
		return mileage_date;
	}
	public void setMileage_date(String mileage_date) {
		this.mileage_date = mileage_date;
	}

}
